namespace hyperengine::vk {
    VkImageView makeImageView(Context& context, VkImage image, VkFormat format, VkImageAspectFlags aspectFlags, uint32_t mipLevels) {
        VkImageViewCreateInfo info{};
        info.sType = VK_STRUCTURE_TYPE_IMAGE_VIEW_CREATE_INFO;
        info.image = image;
        info.viewType = VK_IMAGE_VIEW_TYPE_2D;
        info.format = format;
        info.subresourceRange.aspectMask = aspectFlags;
        info.subresourceRange.baseMipLevel = 0;
        info.subresourceRange.levelCount = mipLevels;
        info.subresourceRange.baseArrayLayer = 0;
        info.subresourceRange.layerCount = 1;

        VkImageView view;
        if (vkCreateImageView(context.mDevice, &info, nullptr, &view) != VK_SUCCESS) {
            throw std::runtime_error("failed to create texture image view!");
        }

        return view;
    }

    VkSampler makeSampler(Context& context, float maxLod) {
        VkPhysicalDeviceProperties properties{};
        vkGetPhysicalDeviceProperties(context.mPhysicalDevice, &properties);

        VkSamplerCreateInfo samplerInfo{};
        samplerInfo.sType = VK_STRUCTURE_TYPE_SAMPLER_CREATE_INFO;
        samplerInfo.magFilter = VK_FILTER_LINEAR;
        samplerInfo.minFilter = VK_FILTER_LINEAR;
        samplerInfo.addressModeU = VK_SAMPLER_ADDRESS_MODE_REPEAT;
        samplerInfo.addressModeV = VK_SAMPLER_ADDRESS_MODE_REPEAT;
        samplerInfo.addressModeW = VK_SAMPLER_ADDRESS_MODE_REPEAT;
        samplerInfo.anisotropyEnable = VK_TRUE;
        samplerInfo.maxAnisotropy = properties.limits.maxSamplerAnisotropy;
        samplerInfo.borderColor = VK_BORDER_COLOR_INT_OPAQUE_BLACK;
        samplerInfo.unnormalizedCoordinates = VK_FALSE;
        samplerInfo.compareEnable = VK_FALSE;
        samplerInfo.compareOp = VK_COMPARE_OP_ALWAYS;
        samplerInfo.mipmapMode = VK_SAMPLER_MIPMAP_MODE_LINEAR;
        samplerInfo.minLod = 0.0f;
        samplerInfo.maxLod = maxLod;
        samplerInfo.mipLodBias = 0.0f;

        VkSampler sampler;
        if (vkCreateSampler(context.mDevice, &samplerInfo, nullptr, &sampler) != VK_SUCCESS) {
            throw std::runtime_error("failed to create texture sampler!");
        }

        return sampler;
    }
}

struct Vertex {
    glm::vec3 pos;
    glm::vec3 color;
    glm::vec2 texCoord;

    static VkVertexInputBindingDescription getBindingDescription() {
        VkVertexInputBindingDescription bindingDescription{};
        bindingDescription.binding = 0;
        bindingDescription.stride = sizeof(Vertex);
        bindingDescription.inputRate = VK_VERTEX_INPUT_RATE_VERTEX;

        return bindingDescription;
    }

    static std::array<VkVertexInputAttributeDescription, 3> getAttributeDescriptions() {
        std::array<VkVertexInputAttributeDescription, 3> attributeDescriptions{};

        attributeDescriptions[0].binding = 0;
        attributeDescriptions[0].location = 0;
        attributeDescriptions[0].format = VK_FORMAT_R32G32B32_SFLOAT;
        attributeDescriptions[0].offset = offsetof(Vertex, pos);

        attributeDescriptions[1].binding = 0;
        attributeDescriptions[1].location = 1;
        attributeDescriptions[1].format = VK_FORMAT_R32G32B32_SFLOAT;
        attributeDescriptions[1].offset = offsetof(Vertex, color);

        attributeDescriptions[2].binding = 0;
        attributeDescriptions[2].location = 2;
        attributeDescriptions[2].format = VK_FORMAT_R32G32_SFLOAT;
        attributeDescriptions[2].offset = offsetof(Vertex, texCoord);

        return attributeDescriptions;
    }

    bool operator==(const Vertex& other) const {
        return pos == other.pos && color == other.color && texCoord == other.texCoord;
    }
};

template<> struct std::hash<Vertex> {
    size_t operator()(Vertex const& vertex) const {
        return ((hash<glm::vec3>()(vertex.pos) ^ (hash<glm::vec3>()(vertex.color) << 1)) >> 1) ^ (hash<glm::vec2>()(vertex.texCoord) << 1);
    }
};

struct MeshData final {
    void load(char const* filepath) {
        vertices.clear();
        indices.clear();

        Assimp::Importer import;
        aiScene const* scene = import.ReadFile(filepath,
            aiProcess_JoinIdenticalVertices | aiProcess_CalcTangentSpace | aiProcess_GenSmoothNormals |
            aiProcess_GenUVCoords | aiProcess_Triangulate | aiProcess_RemoveComponent |
            aiProcess_OptimizeGraph | aiProcess_OptimizeMeshes | aiProcess_ImproveCacheLocality |
            aiProcess_FixInfacingNormals);

        if (!scene || scene->mFlags & AI_SCENE_FLAGS_INCOMPLETE || !scene->mRootNode) {
            spdlog::error(import.GetErrorString());
            return;
        }

        if (scene->mNumMeshes != 1) {
            spdlog::error("Only one mesh can be exported currently");
            return;
        }

        aiMesh* mesh = scene->mMeshes[0];

        if (!mesh->HasTextureCoords(0)) {
            spdlog::warn("No texture coordinates found in mesh: {}", MODEL_PATH.c_str());
        }

        vertices.reserve(mesh->mNumVertices);

        for (unsigned int i = 0; i < mesh->mNumVertices; ++i) {
            Vertex vertex{};

            if (mesh->HasTextureCoords(0)) {
                auto& textureCoord = mesh->mTextureCoords[0][i];
                vertex.texCoord = glm::vec2(textureCoord.x, textureCoord.y);
            }

            vertex.color = { 1, 1, 1 };
            vertex.pos = std::bit_cast<glm::vec3>(mesh->mVertices[i]);

            vertices.push_back(vertex);
        }

        indices.reserve(mesh->mNumFaces * 3);

        for (unsigned int iFace = 0; iFace < mesh->mNumFaces; iFace++) {
            aiFace face = mesh->mFaces[iFace];
            if (face.mNumIndices != 3) continue;

            for (unsigned int iElement = 0; iElement < 3; ++iElement) {
                indices.push_back(face.mIndices[iElement]);
            }
        }
    }

    std::vector<Vertex> vertices;
    std::vector<uint32_t> indices;
};

struct GpuMesh final {

    void clean(VkDevice& device) {
        vkDestroyBuffer(device, mVertexBuffer, nullptr);
        vkFreeMemory(device, mVertexBufferMemory, nullptr);
        vkDestroyBuffer(device, mIndexBuffer, nullptr);
        vkFreeMemory(device, mIndexBufferMemory, nullptr);
    }

    VkBuffer mVertexBuffer;
    VkDeviceMemory mVertexBufferMemory;
    VkBuffer mIndexBuffer;
    VkDeviceMemory mIndexBufferMemory;
    int mIndicesCount;
};

class Image final {
public:
    constexpr Image() = default;
    Image(char const* filepath) {
        stbi_set_flip_vertically_on_load(true);
        mPixels = stbi_load(filepath, &mWidth, &mHeight, nullptr, STBI_rgb_alpha);
    }

    Image(Image const&) = delete;
    Image& operator=(Image const&) = delete;

    inline Image(Image&& other) noexcept { *this = std::move(other); }
    Image& operator=(Image&& other) noexcept {
        std::swap(mPixels, other.mPixels);
        std::swap(mWidth, other.mWidth);
        std::swap(mHeight, other.mHeight);
        return *this;
    }

    ~Image() noexcept {
        stbi_image_free(mPixels);
    }

    void* pixels() { return mPixels; }
    int size() const { return mWidth * mHeight * STBI_rgb_alpha; }
    int width() const { return mWidth; }
    int height() const { return mHeight; }
private:
    unsigned char* mPixels = nullptr;
    int mWidth = 0, mHeight = 0;
};

